export function formatNumber(num: number): string {
  if (num >= 1e9) {
    return (num / 1e9).toFixed(1) + 'B';
  }
  if (num >= 1e6) {
    return (num / 1e6).toFixed(1) + 'M';
  }
  if (num >= 1e3) {
    return (num / 1e3).toFixed(1) + 'K';
  }
  return num.toString();
}

export function formatCurrency(amount: number, currency: string = 'ETH'): string {
  const ethValue = amount / 1e18; // Convert from wei to ETH
  if (ethValue >= 1e6) {
    return `${(ethValue / 1e6).toFixed(2)}M ${currency}`;
  }
  if (ethValue >= 1e3) {
    return `${(ethValue / 1e3).toFixed(2)}K ${currency}`;
  }
  return `${ethValue.toFixed(4)} ${currency}`;
}

export function formatAddress(address: string): string {
  if (address.length <= 10) return address;
  return `${address.slice(0, 6)}...${address.slice(-4)}`;
}

export function formatTimestamp(timestamp: Date): string {
  const now = new Date();
  const diff = now.getTime() - timestamp.getTime();
  const minutes = Math.floor(diff / 60000);
  const hours = Math.floor(diff / 3600000);
  const days = Math.floor(diff / 86400000);

  if (days > 0) return `${days}d ago`;
  if (hours > 0) return `${hours}h ago`;
  if (minutes > 0) return `${minutes}m ago`;
  return 'Just now';
}